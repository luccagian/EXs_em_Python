"""
Escreva um programa para ler a base e a altura de um triângulo. Em seguida, escreva o
valor da área do triângulo.
Obs.: área = (base * altura) / 2
"""

base = float(input("Escolha sua base: "))
altura = float(input("Escolha sua altura: "))

area = (base * altura) / 2

print("Area do triangulo é ", area)
